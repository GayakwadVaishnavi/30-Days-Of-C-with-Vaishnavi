#include <stdio.h>

int main()
{
    int i;
    int j;
    printf("......Printing Numbers From 10 to 1..........\n");
    for(i = 10; i >= 1; i--)
    {
        printf("%d\n",i);

    }
    printf("......Printing Numbers from 1 to 10.........\n");
    for(j = 1; j <=10 ; j++)
    {
        printf("%d\n",j);
    }
    return 0;
}
